#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		if (*(char *)(s + i) == (char)c)
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
