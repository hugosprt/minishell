#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*cpy;

	i = 0;
	cpy = malloc(sizeof(char) * (ft_strlen((char *)s) + 1));
	if (!cpy)
		return (NULL);
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
