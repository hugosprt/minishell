#include "libft.h"

int	ft_atoi(const char *s)
{
	long long int	i;
	int				sign;
	long long int	nb;

	sign = 1;
	i = 0;
	nb = 0;
	while (s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\r' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (sign == -1 || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = (nb * 10) + (s[i] - '0');
		i++;
	}
	return (nb * sign);
}
